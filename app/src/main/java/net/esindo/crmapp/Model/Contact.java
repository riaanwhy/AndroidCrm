public class Contact {
    String no;
    String name;
    String image;
    String phone;

    public Contact() {
    }

    public String getNo() {
        return no;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public String getPhone() {
        return phone;
    }
}
